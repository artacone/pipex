#include <libft.h>

char	*ft_strchr(const char *s, int c)
{
	char	ch;

	ch = c;
	while (*s)
	{
		if (*s++ == ch)
		{
			return ((char *)s - 1);
		}
	}
	if (ch == '\0')
	{
		return ((char *)s);
	}
	return (NULL);
}
